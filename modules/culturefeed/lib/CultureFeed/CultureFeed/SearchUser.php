<?php

/**
 * Class to represent a user as returned in a search operation.
 */
class CultureFeed_SearchUser {

  /**
   * ID of the user.
   *
   * @var string
   */
  public $id;

  /**
   * Nick of the user.
   *
   * @var string
   */
  public $nick;

  /**
   * Depiction of the user.
   *
   * @var string
   */
  public $depiction;

  /**
   * Soft value.
   *
   * @var integer
   */
  public $sortValue;

}

